package kr.or.ddit.study01;

/** alt+shift+j 도큐먼트 주석 단축키
 *  ShortCut 위에서 F2
 * 
 * 단축키 안내 클래스 입니다.
 *
 */

public class ShortCut {
	public static void main(String[] args) {
		/*
		 * 	단축키
		 * 	ctrl+shift+l : 모든 단축키 안내
		 * 
		 * 	ctrl + space : 자동완성
		 * 
		 *  ctrl + shift + / : 범위주석 (범위설정해야, 줄이 바뀜)
		 *  ctrl + shift + c : 한줄주석/취소
		 *  ctrl + /		 : 한줄주석/취소
		 *  
		 *  ctrl + n : 신규 생성
		 *  
		 *  ctrl + a : 전체선택
		 *  
		 *  ctrl + shift + f : 정렬 (범위설정)
		 *  
		 *  ctrl + alt + 방향키 : 한줄 복사
		 *  ctrl + d : 한줄 삭제
		 *  
		 *  ctrl + z : 실행취소
		 *  ctrl + y : 실행취소의 취소
		 *  
		 *  ctrl + f : 찾기
		 *  ctrl + h : 전체 파일에서 찾기
		 *  
		 *  ctrl + s : 저장
		 *  ctrl + shift + s : 전체저장  
		 *  
		 *  ctrl + F11 : 소스 실행
		 *  
		 *  alt + shift + r : 변수 전체 이름 변경
		 *  
		 *  alt + shift + a : 여러줄입력
		 *  
		 *  alt + 방향키 : 순서 위아래로 바꿈
		 *  
		 *  ctrl + shift + enter : 위로 줄 추가
		 *  
		 *  alt + shift + s : Generate toString() 불러옴
		 *  
		 */

		int x2;
		x2 = 2;
		int y = 4;
		System.out.println(x2+y);
		
		
//		System.out.println(1);
		System.out.println(2);
		System.out.println(2);

	}
}
